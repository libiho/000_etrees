package com.spring.mbti.vo;

public class MbtiVo {
	
	private int mbtiNum;
	private String mbtiTwoType;
	private String mbtiQuiz;
	private String 	creator;
	private String	modifier;
	
	
	public int getMbtiNum() {
		return mbtiNum;
	}
	public void setMbtiNum(int mbtiNum) {
		this.mbtiNum = mbtiNum;
	}
	public String getMbtiTwoType() {
		return mbtiTwoType;
	}
	public void setMbtiTwoType(String mbtiTwoType) {
		this.mbtiTwoType = mbtiTwoType;
	}
	public String getMbtiQuiz() {
		return mbtiQuiz;
	}
	public void setMbtiQuiz(String mbtiQuiz) {
		this.mbtiQuiz = mbtiQuiz;
	}
	public String getCreator() {
		return creator;
	}
	public void setCreator(String creator) {
		this.creator = creator;
	}
	public String getModifier() {
		return modifier;
	}
	public void setModifier(String modifier) {
		this.modifier = modifier;
	}

}
